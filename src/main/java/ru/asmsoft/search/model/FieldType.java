package ru.asmsoft.search.model;

/** Available field types. */
public enum FieldType {

  /** Boolean field type. */
  BOOLEAN,

  /** Byte field type. */
  BYTE,

  /** Character field type. */
  CHARACTER,

  /** Short field type. */
  SHORT,

  /** Integer field type. */
  INTEGER,

  /** Long field type. */
  LONG,

  /** Float field type. */
  FLOAT,

  /** Double field type. */
  DOUBLE,

  /** BigInteger field type. */
  BIGINTEGER,

  /** BigDecimal field type. */
  BIGDECIMAL,

  /** Date field type. */
  DATE,

  /** DateTime field type. */
  DATETIME,

  /** String field type. */
  STRING
}
